    <script>
document.getElementById('contactForm').addEventListener('submit', function (e) {
    e.preventDefault();

    const form = this;
    const formData = new FormData(form);

    fetch(form.action, {
        method: 'POST',
        body: formData
    })
    .then(response => response.text())
    .then(result => {

        // success toast
        Toastify({
            text: "Message sent successfully!",
            duration: 3000,
            gravity: "top",
            position: "right",
            close: true,
            backgroundColor: "#3f6cffff"
        }).showToast();

        form.reset();
    })
    .catch(error => {

        // error toast
        Toastify({
            text: "Something went wrong. Please try again!",
            duration: 3000,
            gravity: "top",
            position: "right",
            close: true,
            backgroundColor: "#dc3545"
        }).showToast();
    });
});
</script>




